class TagIdentifier():
    @staticmethod
    def getNamespaceId():
        return "namespace"

    @staticmethod
    def getClassId():
        return "class"

    @staticmethod
    def getStructId():
        return "struct"

    @staticmethod
    def getEnumId():
        return "enum"

    @staticmethod
    def getEnumValueId():
        return "enum_value"

    @staticmethod
    def getUnionId():
        return "union"

    @staticmethod
    def getClassStructUnionMemberId():
        return "class_struct_union_member"

    @staticmethod
    def getLocalVariableId():
        return "local_variable"

    @staticmethod
    def getVariableDefinitionId():
        return "variable_definition"

    @staticmethod
    def getFunctionPrototypeId():
        return "function_prototype"

    @staticmethod
    def getFunctionDefinitionId():
        return "function_definition"

    @staticmethod
    def getMacroId():
        return "macro"

    @staticmethod
    def getTypedefId():
        return "typedef"

    @staticmethod
    def getExternFwdDeclarationId():
        return "extern_fwd_declaration"

